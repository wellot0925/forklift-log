async function seed() {
  console.log(`총 ${tips.length}개 팁 삽입 시작...`)
  for (let i = 0; i < tips.length; i++) {
    const tip = tips[i]
    const ts = Timestamp.fromDate(tip.date)
    const docRef = doc(collection(db, 'tips'))
    await setDoc(docRef, {
      title: tip.title,
      content: tip.content,
      photoUrls: [],
      createdBy: AUTHOR,
      modifiedBy: AUTHOR,
      createdAt: ts,
      updatedAt: ts,
    })
    console.log(`[${i + 1}/${tips.length}] ✅ ${tip.title}`)
  }
  console.log('\n완료! 모든 팁이 Firebase에 저장됐습니다.')
  process.exit(0)
}

seed().catch(err => {
  console.error('오류 발생:', err)
  process.exit(1)
})
